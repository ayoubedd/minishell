#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*res;
	size_t	i;

	res = (char *)s;
	if (n == 0)
		return ;
	i = 0;
	while (i < n)
	{
		res[i] = 0;
		i++;
	}
	return ;
}
